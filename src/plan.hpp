#pragma once
// ----------------------------------------------------------------------------
// Планирование памяти.
//
// Ключевая идея всего решения: вершинные массивы (rank, contrib, out-degree,
// битовая карта существования) НЕ обязаны помещаться в память целиком.
// Диапазон вершин [0, V) разбивается на P интервалов ("слайсов") по S вершин,
// и в памяти одновременно находятся максимум два слайса.
//
// Пиковое потребление памяти — фаза обхода колонок сетки:
//     next-слайс:    8*S байт (atomic<double>)
//     contrib-слайс: 8*S байт (double)
//     exist-слайс:   S/8 байт
//     2 буфера рёбер (двойная буферизация чтения): 2*EDGE_BUF
// => S подбирается так, чтобы 16*S + 2*EDGE_BUF влезло в бюджет минус
//    фиксированный резерв на всё остальное (стеки потоков, индекс сегментов,
//    буферы записи, аллокатор).
//
// Если весь диапазон вершин помещается в один слайс (S >= V), схема
// автоматически вырождается в полу-внешний (semi-external) режим: P = 1,
// вершинные массивы целиком в RAM, рёбра стримятся с диска. Отдельного
// кода для этого случая нет — это важное свойство дизайна.
// ----------------------------------------------------------------------------

#include "common.hpp"

struct Plan {
    u64 V = 0;            // размер пространства id вершин: maxId + 1
    u64 E = 0;            // число рёбер
    u64 S = 0;            // размер слайса (вершин), кратен 64
    u64 P = 0;            // число слайсов/интервалов
    u64 edgeBufBytes = 0; // размер одного буфера чтения рёбер
    u64 writeBufBytes = 0;// размер буфера на один bucket-файл при разбиении
    u64 budgetBytes = 0;  // полный бюджет памяти

    u64 sliceLen(u64 i) const {           // длина i-го слайса (последний короче)
        u64 base = i * S;
        return (base + S <= V) ? S : (V - base);
    }
    u64 sliceOf(i32 v) const { return u64(u32(v)) / S; }
};

// Округление вниз/вверх до кратного 64 (чтобы границы слайсов были выровнены
// по байтам в битовой карте существования: 64 вершины = 8 байт).
inline u64 floor64(u64 x) { return x & ~u64(63); }
inline u64 ceil64(u64 x)  { return (x + 63) & ~u64(63); }

inline Plan makePlan(u64 V, u64 E, u64 memoryMB, u64 sliceOverride) {
    if (memoryMB < 64) die("--memory-mb: минимум 64 МБ");

    Plan p;
    p.V = V;
    p.E = E;
    p.budgetBytes = memoryMB << 20;
    p.edgeBufBytes = 8u << 20; // 8 МБ: достаточно для последовательного SSD I/O

    const u64 reserve = 32u << 20;              // фиксированный резерв
    const u64 avail = p.budgetBytes - reserve;  // на крупные структуры

    if (sliceOverride > 0) {
        // Тестовый режим: принудительно мелкие слайсы, чтобы проверять
        // многослайсовую логику на маленьких графах.
        p.S = ceil64(sliceOverride);
    } else {
        u64 s = (avail - 2 * p.edgeBufBytes) / 16;
        p.S = floor64(s);
        if (p.S < 64) die("слишком маленький бюджет памяти");
    }
    if (p.S >= V) p.S = ceil64(V); // всё влезает в один слайс => P = 1

    p.P = (V + p.S - 1) / p.S;
    if (p.P > 4096)
        die("слишком много интервалов разбиения (P > 4096) — увеличьте --memory-mb");

    // Буфер записи на каждый из P bucket-файлов при разбиении рёбер.
    u64 wb = avail / 2 / (p.P ? p.P : 1);
    if (wb > (4u << 20)) wb = 4u << 20;
    if (wb < (16u << 10)) wb = 16u << 10;
    p.writeBufBytes = wb & ~u64(7); // кратно 8 (размеру ребра)

    return p;
}
